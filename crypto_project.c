int diffie_hellman_fun(unsigned int a1, unsigned int n)
{
    int t_0 = 1;
    while (n > 0)
    {
        if ((n & 1) != 0)
        {
            t_0 = t_0 * a1;
            t_0 = t_0 % P;
        }
        a1 = a1 * a1;
        a1 = a1 % P;
        n = n >> 1;
    }
    return t_0;
}



BYTES *calcMAC(BYTES MAC[][4], BYTES M[][4], BYTES K[][4]){
    for(int i=0;i<4;i++) {
		for(int j=0;j<4;j++) {
            MAC[i][j] = M[i][j] ^ K[i][j];
        }
	}
}



