void addRoundKey(BYTES state[][4], BYTES key[][4])
{
	int i,j;

	for(j=0;j<4;j++) {
		for(i=0;i<4;i++) {
			state[i][j]=state[i][j]^key[i][j];
		}
	}
}

void keyExpansion(BYTES key[][4], BYTES keyExp[11][4][4])
{
    for(int k=0; k<11; k++){
        int i,j;
        for(i=0;i<4;i++) {
            for(j=0;j<4;j++) {
                keyExp[k][i][j]=key[i][j];
            }
        }

        BYTES mtx[4];
        for(i=0;i<4;i++) {
            mtx[i]=key[(i+1)%4][3];
        }

        for(i=0;i<4;i++) {
            mtx[i]=sBox[mtx[i]];
        }

        for(i=0;i<4;i++) {
            key[i][0]=key[i][0]^mtx[i]^Rcon[i][k];
        }

        for(j=1;j<4;j++) {
            for(i=0;i<4;i++) {
                key[i][j]=key[i][j]^key[i][j-1];
            }
        }
    }
}

