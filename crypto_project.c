BYTES *encryptAES(BYTES message[][4], BYTES key[][4]){
    BYTES keyExp[11][4][4];
    keyExpansion(key, keyExp);
	addRoundKey(message, keyExp[0]);

	for(int i=0; i<9; i++) {
		subBytes(message);
		shiftRows(message);
		mix_columns(message);
		addRoundKey(message, keyExp[i+1]);
	}

	subBytes(message);
	shiftRows(message);
	addRoundKey(message, keyExp[10]);

	return message;
}

BYTES *decryptAES(BYTES message[][4], BYTES key[][4]){
    BYTES keyExp[11][4][4];
    keyExpansion(key, keyExp);
	addRoundKey(message, keyExp[10]);

	for(int i=9; i>0; i--) {
        invShiftRows(message);
		invSubBytes(message);
		addRoundKey(message, keyExp[i]);
		inv_mix_columns(message);
	}
	invShiftRows(message);
	invSubBytes(message);
	addRoundKey(message, keyExp[0]);

	return message;
}

